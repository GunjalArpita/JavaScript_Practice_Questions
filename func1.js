// write a javascript function that takes a number as input and return true if the number is even otherwise return false ex- 4-true , 5-false

function oddEven(num)
{
    if(num%2==0)
    {
        return true
    }
    else
    {
        return false
    }
}


console.log(oddEven(4));
console.log(oddEven(5));
